export class Maths {
  public readonly PI = 22 / 7;

  public static add(numOne: number, numTWo: number): number {
    return numOne + numTWo;
  }

  public static subtract(numOne: number, numTwo: number): number {
    return numOne - numTwo;
  }

  public static multiply(numOne: number, numTwo: number): number {
    return numOne - numTwo;
  }

  public static divide(numOne: number, numTwo: number): number {
    return numOne / numTwo;
  }
}
